package section6;

public class Ceiling {
    private double height;
    private double paintedColor;

    public Ceiling(double height, double paintedColor) {
        this.height = height;
        this.paintedColor = paintedColor;
    }

    public double getHeight() {
        return height;
    }

    public double getPaintedColor() {
        return paintedColor;
    }
}
